def sync_arena_telemetry() -> None:
    """
    Full telemetry refresh cycle.

    Execution order
    ---------------
    1. Try to **read** existing state from Firebase.  If found and sufficiently
       fresh (< 30 s), skip re-sampling sensors to avoid thrash.
    2. **Sample** all simulated IoT sensors (gates, restrooms, food).
    3. Compute composite routing scores.
    4. **Write** updated state back to Firebase (best-effort; failures logged).
    5. Mirror state into the local ``arena_state`` dict so routes.py can
       respond without an additional Firebase round-trip.

    Side-effects:
        Mutates :data:`arena_state`, :data:`_history`, and :data:`_arrivals`.
    """
    logger.info(
        "NEXUS-ENGINE: telemetry sync #%d", arena_state["telemetry_id"] + 1
    )

    # 1. Try Firebase read first (avoids unnecessary random re-sampling)
    remote = _firebase_read()
    if remote:
        age = time.time() - remote.get("last_updated", 0)
        if age < 30:
            # State is fresh; update local mirror and skip sensor re-sample
            arena_state.update(remote)
            arena_state["telemetry_id"] = remote.get("telemetry_id", 0)
            logger.info("NEXUS-ENGINE: used cached Firebase state (age=%.1fs)", age)
            return

    # 2. Sample sensors
    arena_state["telemetry_id"] += 1
    arena_state["last_updated"]  = time.time()

    raw_gates: List[Dict[str, Any]] = [
        {"id": "Gate A · North Concourse", "distance_m": 120,
         "lat": 40.8140, "lng": -74.0750, "type": "GA",
         "wait": random.randint(18, 45)},
        {"id": "Gate B · South Entry",     "distance_m": 230,
         "lat": 40.8120, "lng": -74.0740, "type": "GA",
         "wait": random.randint(5, 18)},
        {"id": "Gate C · VIP East Deck",   "distance_m": 310,
         "lat": 40.8130, "lng": -74.0730, "type": "VIP",
         "wait": random.randint(2, 8)},
    ]

    _simulate_arrivals(raw_gates)

    for g in raw_gates:
        gid  = g["id"]
        wait = g["wait"]
        dist = g["distance_m"]

        walk_time = dist / WALKING_SPEED_M_PER_MIN
        vel_pen   = _crowd_velocity_penalty(wait, dist)
        decay_adj = _historical_decay_bonus(gid, wait)
        future_wt = _predictive_arrival_load(gid)

        raw_score          = wait + walk_time + vel_pen + decay_adj + future_wt
        g["efficiency_score"] = round(max(float(wait), raw_score), 2)
        g["is_best"]          = False
        g["walk_min"]         = round(walk_time, 1)
        g["vel_penalty"]      = vel_pen
        g["future_weight"]    = future_wt

        _record_history(gid, wait)

    arena_state["gates"] = raw_gates

    arena_state["restrooms"] = [
        {"name": "Lower Deck A",      "wait": random.randint(1, 10), "status": "Clean"},
        {"name": "VIP Lounge North",  "wait": random.randint(0,  4), "status": "Premium"},
        {"name": "Upper Concourse B", "wait": random.randint(2, 14), "status": "Clean"},
    ]
    arena_state["food_services"] = [
        {"name": "Victory Burgers",   "wait": random.randint(10, 30), "popularity": "High"},
        {"name": "Espresso Pit Stop", "wait": random.randint(2,   8), "popularity": "Medium"},
        {"name": "Nacho Libre Tacos", "wait": random.randint(5,  15), "popularity": "Medium"},
        {"name": "Quick-Sip Bar",     "wait": random.randint(1,   6), "popularity": "Low"},
    ]

    # 4. Persist to Firebase (non-blocking best-effort)
    wrote = _firebase_write(arena_state)
    logger.info(
        "NEXUS-ENGINE: sync complete — gates=%d firebase_write=%s",
        len(arena_state["gates"]), wrote,
    )


def build_recommendation(vip_enabled: bool = False) -> Dict[str, str]:
    """
    Determine the optimal gate and generate a routing protip.

    Attempts to generate the protip via Gemini AI; falls back to the
    deterministic narrative algorithm if the API is unavailable or slow.

    When ``vip_enabled`` is ``False``, VIP-type gates are excluded from the
    OPTIMAL selection but remain visible in the gate list.

    Args:
        vip_enabled: Whether the requesting user has VIP access unlocked.

    Returns:
        Dictionary with keys ``headline``, ``detail``, and ``action``.
    """
    try:
        eligible: List[Dict[str, Any]] = (
            arena_state["gates"] if vip_enabled
            else [g for g in arena_state["gates"] if g["type"] != "VIP"]
        ) or arena_state["gates"]

        best = min(eligible, key=lambda g: g["efficiency_score"])

        for g in arena_state["gates"]:
            g["is_best"] = g["id"] == best["id"]

        alt_list = [g for g in eligible if g["id"] != best["id"]]
        alt: Optional[Dict[str, Any]] = (
            min(alt_list, key=lambda g: g["efficiency_score"]) if alt_list else None
        )

        # ── Try Gemini first ──────────────────────────────────────────────────
        ai_tip = _gemini_protip(arena_state["gates"])
        if ai_tip and all(k in ai_tip for k in ("headline", "detail", "action")):
            ai_tip.setdefault("action", f"Navigate to {best['id']}")
            return ai_tip

        # ── Deterministic fallback ────────────────────────────────────────────
        vip_pfx = "👑 VIP Routing — " if (vip_enabled and best["type"] == "VIP") else ""

        if alt:
            wait_delta = alt["wait"] - best["wait"]
            dist_delta = best["distance_m"] - alt["distance_m"]
            if wait_delta >= 8 and abs(dist_delta) < 100:
                headline = f"{vip_pfx}{best['id']} is {wait_delta} mins faster."
                detail   = (
                    f"NEXUS composite: {best['efficiency_score']} vs "
                    f"{alt['efficiency_score']}. Velocity + predictive load factored. "
                    f"Move now — queue is trending clear."
                )
            elif abs(wait_delta) <= 3 and dist_delta < -80:
                headline = f"{vip_pfx}{best['id']} & {alt['id']}: near-identical queues."
                detail   = (
                    f"{alt['id']} is {abs(dist_delta)}m closer. "
                    f"Total transit savings: {alt['efficiency_score']} min score."
                )
            else:
                headline = f"{vip_pfx}{best['id']} is your optimal route."
                detail   = (
                    f"Score {best['efficiency_score']} min (wait + walk + density + "
                    f"+{best['future_weight']}m inbound). "
                    f"Saves ~{round(alt['efficiency_score'] - best['efficiency_score'], 1)}m vs next best."
                )
        else:
            headline = f"{vip_pfx}{best['id']} is optimal."
            detail   = (
                f"Score {best['efficiency_score']} min. "
                f"Predictive arrival load factored in."
            )

        return {"headline": headline, "detail": detail, "action": f"Navigate to {best['id']}"}

    except Exception as exc:
        logger.error("Recommendation failure: %s", exc)
        return {
            "headline": "Intelligence System Syncing",
            "detail":   "Recalculating safest routes…",
            "action":   "Stand by",
        }


def crowd_score() -> int:
    """
    Compute a 0-100 crowd-flow score (higher = less congested).

    Derived from the mean raw wait across all gates, normalised against
    :data:`MAX_WAIT_SCALING_FACTOR`.

    Returns:
        Integer crowd score clamped to [0, 100].
    """
    if not arena_state["gates"]:
        return 50
    avg = sum(g["wait"] for g in arena_state["gates"]) / len(arena_state["gates"])
    return max(0, min(100, round(100 - (avg / MAX_WAIT_SCALING_FACTOR) * 100)))


